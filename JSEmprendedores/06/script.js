function convertidor(gradosCelcius) {
  var gradosF = gradosCelcius * 9/5 + 32;
  return gradosF;
}

console.log(convertidor(30));
console.log(convertidor(40));
console.log(convertidor(20));
console.log(convertidor(35));

function multiplicar(tablaDel) {
  var producto = tablaDel * 2;
  return producto;
}

console.log(multiplicar(1));
console.log(multiplicar(2));
console.log(multiplicar(3));
console.log(multiplicar(4));
console.log(multiplicar(5));
